void main() {
  int age = 30;

  switch (age) {
    case 5:
      {
        print("A toddler");
      }
      break;

    case 13:
      {
        print("A pre-teen and full of hope");
      }
      break;

    case 18:
      {
        print("Welcome to adulthood. Assume you know nothing");
      }
      break;

    case 30:
      {
        print("A full grown-up");
      }
      break;

    case 40:
      {
        print("Wise at a last");
      }
      break;
  }
}
